
/**
 * Represents a player move in a Tic-tac-game.

 * @author Chaz Harrison
 * @version 10/30/21
 */
public class Move
{
    /**
     * The move's row
     */
    int row;
    /**The Move's column 
     * 
     */
    int col;
    
    //constructor
    /**
     * Constructor for objects of class Move.

     */
    public Move(int row, int col){
        this.row = row;
        this.col = col;
        
    }
    
}
